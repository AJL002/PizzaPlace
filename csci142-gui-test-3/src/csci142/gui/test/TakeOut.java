    private void btnClose1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClose1ActionPerformed
        // TODO add your handling code here:
        if(tfNumber.getText().isBlank() || tfName.getText().isBlank())
        {
            JOptionPane.showMessageDialog(this, "ERROR: One or more required textfields are empty");
        }
        
        else
        {
        Customer takeout = new Customer(tfName.getText(), tfNumber.getText(), null);
        takeout.toCSV(customers, takeout);

        this.dispose();
        Menu mn = new Menu(takeout);
        mn.setVisible(true);
        mn.setTitle("Pizza Place POS System - Menu");
        }
    }//GEN-LAST:event_btnClose1ActionPerformed

