    private void btnFinishActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinishActionPerformed
        // TODO add your handling code here:
        
        if (subtotal == 0)
        {
            System.out.println("ERROR NO ITEMS SELECTED");
            JOptionPane.showMessageDialog(this, "ERROR: Please select one or more items");
        
        }
        
        else if(!jbtnCash.isSelected() && !jbtnCard.isSelected())
        {
            System.out.println("ERROR NO PAYMENT TYPE SELECTED");
            JOptionPane.showMessageDialog(this, "ERROR: No payment type selected");
        }
        
        else
        {
            if(jbtnCash.isSelected())
            {
                ProcessOrderCash order = new ProcessOrderCash(orderdetails, total, model);
                
                this.dispose();
                
                order.setVisible(true);
            
            }

            if(jbtnCard.isSelected())
            {
                ProcessOrderCard order = new ProcessOrderCard(orderdetails, total, model);
                this.dispose();
                order.setVisible(rootPaneCheckingEnabled);
            
            }
        }
        
        
    }//GEN-LAST:event_btnFinishActionPerformed

