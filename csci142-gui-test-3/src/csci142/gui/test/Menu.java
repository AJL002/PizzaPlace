    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        model = (DefaultTableModel)tblTable.getModel();
        if(!tblTable.getSelectionModel().isSelectionEmpty()){
            String name = model.getValueAt(tblTable.getSelectedRow(), 0).toString(); //retrieving item name from first column
            Float item = order.get(name);
            subtotal -= item;
            
            model.removeRow(tblTable.getSelectedRow());
            
            refresh();
            
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

