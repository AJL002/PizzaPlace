 private void refresh()
 {
    DecimalFormat frm = new DecimalFormat("##.##");
    
    
    String newsub = frm.format(subtotal); //Formats subtotal
    Float stfloat = Float.parseFloat(newsub); //Converts subtotal back to float
    lblSubtotalNum.setText(newsub); // sets formatted string subtotal to lbl
    
    Float tax = tax(stfloat);
    String newtax = frm.format(tax);
    lblTaxNum.setText(newtax);
    
    total = subtotal + tax;
    String newtotal = frm.format(total);
    lblTotalNum.setText(newtotal);
    
    jScrollPane5.repaint();
 }
 


