    private void btnReceiptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReceiptActionPerformed
        // TODO add your handling code here:

        
        
        if (tfPayment.getText().isBlank())
        {
            JOptionPane.showMessageDialog(this, "ERROR: No payment has been entered");
        }
        
        
        else if (Float.parseFloat(tfPayment.getText()) < total)
        {
            JOptionPane.showMessageDialog(this, "ERROR: Payment received is insufficient to complete purchase"); 
        }
        
        else
        {
            Float payment = Float.parseFloat(tfPayment.getText());
            Float change = payment - total;
            
            DecimalFormat frm = new DecimalFormat("##.##");
    
            String newsub = frm.format(change); //Formats subtotal
            change = Float.parseFloat(newsub);
            tip = Float.parseFloat(tfTip.getText());
            
            
            JOptionPane.showMessageDialog(this, "Success! The order has been submitted! Change: " + "$" + change); 
            this.dispose();
            
            printReceipt(customer, total.toString(), tip.toString(), newsub, model);
            
            Order titlescreen = new Order(); //back to the very beginning
            titlescreen.setVisible(true);
        }
    }//GEN-LAST:event_btnReceiptActionPerformed

