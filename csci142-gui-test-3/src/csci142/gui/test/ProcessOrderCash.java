private void printReceipt(Customer person, String total, String tip, String change, DefaultTableModel model)
    {
        Date date=java.util.Calendar.getInstance().getTime();
        
        File receipt = new File("out/receipt-" + java.time.LocalDate.now() + "-" + person.getName() + ".txt");
        
        try
        {
            FileWriter write = new FileWriter(receipt, true);
            BufferedWriter bwrite = new BufferedWriter(write);
            PrintWriter pwrite = new PrintWriter(bwrite);
        
            pwrite.println(date);
            pwrite.println();
            
            pwrite.println("Name: " + person.getName());
            pwrite.println("Phone Number: " + person.getPhoneNumber());
            pwrite.println("Address: " + person.getAddress());
            pwrite.println();
            
            if(person.getAddress() == null)
                pwrite.println("Method: TakeOut");
            else
                pwrite.println("Method: Delivery");
            
            pwrite.println("ORDERED: ");
            pwrite.println(model.getDataVector());
            pwrite.println();
            
            pwrite.println("Tip: $" + tip);
            pwrite.println("Change: $" + change);
            pwrite.println();
            
            pwrite.println("TOTAL: $" + total);
            
            
            pwrite.flush();
            pwrite.close();
        }
        
        catch (IOException e)
        {
            e.printStackTrace();
        }
    }

